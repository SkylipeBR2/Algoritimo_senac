public class ado4 {
    public static void main(String[] args) {
        System.out.printf("Iremos realizar uma Contagem de 10 em 10 até o número 1000, vamos lá");
        for (int n = 10; n <= 1000; n += 10) {
            System.out.println(n);
            System.out.println(".");
        }
    }
}
